marks = [90,99,89,95,94] #creating a list of marks obtained by students in a class
print(marks) #printing the list of marks
marks[2]=98 #updating the mark of the student at index 2 to 98
marks.append(97) #adding a new mark of 97 to the end of the list
marks.insert(2,96) #inserting a new mark of 96 at index 2
marks.remove(99) #removing the mark of 99 from the list
marks.pop(0) #removing the mark at index 0 from the list
print(marks) #printing the updated list of marks